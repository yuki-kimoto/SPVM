  'no-build-spvm-modules' => \my $no_build_spvm_modules,
  
  # For cpan/cpanm to run Makefile.PL
  'meta' => \my $meta,
  
  # Paralle settings
  'parallel-make' => \my $paralle_make,
  'parallel-test' => \my $paralle_test,
  'jobs=i' => \my $jobs,
  
  ########## [SPVM Only Command Line Options Start] ##########
  'no-precompile-tests' => \my $no_precompile_tests,
  ########## [SPVM Only Line Options Options End] ##########
);

my $build_dir = '.spvm_build';
mkpath $build_dir;

my $config_global_file = '.spvm_build/global.config';
&generate_config_global_file($config_global_file, {
  ccflags       => \@ccflags,
  defines       => \@defines,
  ldflags       => \@ldflags,
  debug         => $debug,
  build_type    => $build_type,
  asan_on_linux => $asan_on_linux,
});

my $gnu_make = SPVM::Builder::Util::API::search_gnu_make_command();

if ($meta) {
  $no_build_spvm_modules = 1;
}

if ($debug) {
  $build_type = 'Debug';
}

my $asan_logs_dir = "$FindBin::Bin/.spvm_build/asan_logs";
if ($asan_on_linux) {
  push @ccflags, "-fsanitize=address", "-fno-omit-frame-pointer";
  push @ldflags, "-fsanitize=address";
  rmtree $asan_logs_dir;
  mkpath $asan_logs_dir;
}

# Jobs count for pallalel build and tests
unless (defined $jobs) {
  my $cpus = SPVM::Builder::Util::API::get_cpu_count();
  $jobs = $cpus + 2;
}
# Limit the maximum number of parallel jobs to 16 to prevent Windows from exhausting OS resources, such as process handles and kernel memory, ensuring stable performance during JIT builds.
if ($jobs > 16) {
  $jobs = 16;
}

########## [SPVM Only Build Loigc Start] ##########

# Check supported environment
{
  # SPVM requires a 64-bit address space (pointer size must be 8 bytes)
  # This ensures support for large memory maps required by coroutines.
  my $ptrsize = $Config{ptrsize};
  if ($ptrsize < 8) {
    warn "SPVM doesn't support 32-bit address spaces (the pointer size is $ptrsize bytes). A 64-bit environment is required.\n";
    die "OS unsupported\n";
  }
  
  # SPVM requires GNU Make for the order-only dependency syntax '|' and the 'override' syntax.
  unless ($gnu_make) {
    warn "Building SPVM needs GNU Make.\n";
    die "OS unsupported\n";
  }
}

# Always true in SPVM
$paralle_make = 1;
$paralle_test = 1;

if ($meta) {
  $no_build_spvm_modules = 1;
}

