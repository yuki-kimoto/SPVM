my $default_config = SPVM::Builder::Util::API::create_default_config;

my $config_global = SPVM::Builder::Config::DLL->new;
if (defined $build_type) {
  $config_global->build_type($build_type);
}

$default_config->config_global($config_global);

# Apply config from build type
my $build_type_config = SPVM::Builder::Util::get_config_from_build_type($build_type);
for my $name (keys %$build_type_config) {
  my $value = $build_type_config->{$name};
  $default_config->{$name} = $value;
}

# OPTIMIZE
my $optimize = $default_config->optimize;
unless (length $optimize) {
  $optimize = ' ';
}

# DEFINE
my @define_ccflags = map { "-D$_" } @defines;

# LDDLFLAGS
my $lddlflags = $Config{lddlflags};
# Remove -O[0-9s] to ensure clean debug session
$lddlflags =~ s/(?:^|\s)-O[0-9s]\b//g;
# Remove strip flag (-s) more robustly ---
$lddlflags =~ s/(?:^|\s)-s(?:\s|$)/ /g;
my $ld_optimize = $default_config->ld_optimize;
unless (length $ld_optimize) {
  $ld_optimize = ' ';
}
$lddlflags .= " " . join(' ', @{$default_config->thread_ldflags}, @{$default_config->debug_info_ldflags}, @{$default_config->symbol_strip_ldflags}, $ld_optimize, @ldflags);

# INC
my $spvm_core_include_dir = '-Ilib/SPVM/Builder/include';

# C - Source files
my $spvm_core_source_file_names = SPVM::Builder::Util::get_spvm_core_source_file_names();
my @spvm_core_source_files = map { "lib/SPVM/Builder/src/$_" } @$spvm_core_source_file_names;
my @csource_files = ('SPVM.c', @spvm_core_source_files);

# H - Header files(This is only used to resolve dependencies)
my @spvm_header_files = glob 'lib/SPVM/Builder/include/*.h';
my @header_files = ('ppport.h', @spvm_header_files);

########## [SPVM Only Build Loigc End] ##########

my $object_map = {};
for my $c_file (@csource_files) {
    my $obj_file = $c_file;
    $obj_file =~ s/\.c$/\$(OBJ_EXT)/;
    $object_map->{$obj_file} = $c_file;
}

