package MY {
  
  ########## [SPVM Only Module Loading Start] ##########
  use File::Find 'find';
  ########## [SPVM Only Module Loading End] ##########
  
  # Add Build shared library make rule
  sub postamble {
    
    # The content of Makefile
    my $make_rule = '';
    
    ########## [SPVM Only Compilation Start] ##########
    for my $spvm_core_source_file (@spvm_core_source_files) {
      my $spvm_core_object_file = $spvm_core_source_file;
      $spvm_core_object_file =~ s/\.c$/\$(OBJ_EXT)/;
      
      $make_rule .= "$spvm_core_object_file : $spvm_core_source_file\n";
      
      my $compile_info = SPVM::Builder::CompileInfo->new(config => $default_config);
      
      my $spvm_core_ccflags = $compile_info->create_ccflags;
      
      my $spvm_core_cc = $compile_info->config->cc;
      
      $make_rule .= "\t$spvm_core_cc -c $spvm_core_include_dir @$spvm_core_ccflags -o $spvm_core_object_file $spvm_core_source_file\n\n";
    }
    ########## [SPVM Only Compilation End] ##########
    
    unless ($no_build_spvm_modules) {
      my $options = {};
      if (defined $build_type) {
        $options->{build_type} = $build_type;
      }
      $options->{dependent_files} = ['SPVM.xs'];
      
      $options->{order_only_dependent_files} = ['$(INST_DYNAMIC)', 'pm_to_blib'];
      
      if (@ccflags) {
        $options->{ccflags} = [@ccflags];
      }
      if (@defines) {
        $options->{defines} = [@defines];
      }
      if (@ldflags) {
        $options->{ldflags} = [@ldflags];
      }
      
      $options->{config_global_file} = $config_global_file;
      
      # Test create_make_rule for backward compatibility and practical testing
      $make_rule .= SPVM::Builder::Util::API::create_make_rule_native('Fn', $options);
      $make_rule .= SPVM::Builder::Util::API::create_make_rule_precompile('Fn', $options);
      
      # Native classes (excluding Fn)
      $options->{native_classes} = [
        'Format',
        'Sync::Mutex',
        'Native',
        'Native::Compiler',
        'Native::ClassFile',
        'Native::Runtime',
        'Native::Env',
        'Native::Stack',
        'Native::BasicType',
        'Native::ClassVar',
        'Native::Field',
        'Native::Method',
        'Native::Arg',
        'Native::MethodCall',
        'Native::API',
        'Native::Constant',
      ];

      # Precompile classes (excluding Fn)
      $options->{precompile_classes} = [
        'Format',
        'Sort',
        'Packer',
      ];
      
      # Specify text files containing additional classes for practical tests
      # These files contain newline-separated class names like 'Array'
      $options->{native_classes_file} = 'native_classes.txt';
      $options->{precompile_classes_file} = 'precompile_classes.txt';
      
      # Generate a single parallel make rule for the rest
      $make_rule .= SPVM::Builder::Util::API::create_make_rule_parallel($options);
    }
    
    ########## [SPVM Only Test Generation Start] ##########
    unless ($no_precompile_tests) {
      # Create precompile tests
      my @test_files;
      find(
        sub {
          if (-f $File::Find::name) {
            my $rel_path = $File::Find::name;
            $rel_path =~ s|^\Q$FindBin::Bin/||;
            
            # Slip hidden files
            return if $rel_path =~ /[\/\\]\./;

            push @test_files, $rel_path;
          }
        },
        "$FindBin::Bin/t/02_vm"
      );
      my $test_files_str = join(' ', @test_files);
      
      my $time_stamp_file = "$FindBin::Bin/t/03_precompile/time_stamp.txt";
      
      $make_rule .= "dynamic :: $time_stamp_file\n";
      $make_rule .= "\t\$(NOECHO) \$(NOOP)\n\n";

      $make_rule .= "$time_stamp_file :: t/utils/copy_vm_to_precompile.pl $test_files_str\n";
      $make_rule .= "\tperl t/utils/copy_vm_to_precompile.pl\n";
    }
    ########## [SPVM Only Test Generation End] ##########
    
    return $make_rule;
  }
  
}

sub generate_config_global_file {
  my ($config_global_file, $options) = @_;
  
  my $ccflags       = $options->{ccflags} // [];
  my $defines       = $options->{defines} // [];
  my $ldflags       = $options->{ldflags} // [];
  my $build_type    = $options->{build_type};
  my $debug         = $options->{debug};
  my $asan_on_linux = $options->{asan_on_linux};
  
  # --- Handle options ---
  if ($debug) {
    $build_type = 'Debug';
  }
  
  if ($asan_on_linux) {
    push @$ccflags, "-fsanitize=address", "-fno-omit-frame-pointer";
    push @$ldflags, "-fsanitize=address";
  }
  
  # --- Generate content ---
  my $config_global_content = '';
  
  $config_global_content .= <<"EOS";
use SPVM::Builder::Config::DLL;
my \$config_global = SPVM::Builder::Config::DLL->new;
EOS
  
  if (defined $build_type) {
    $config_global_content .= <<"EOS";
\$config_global->build_type('$build_type');
EOS
  }
  
  for my $ccflag (@$ccflags) {
    $config_global_content .= <<"EOS";
\$config_global->compile_rule_any({'+ccflags' => [$ccflag]});
EOS
  }
  
  for my $define (@$defines) {
    $config_global_content .= <<"EOS";
\$config_global->compile_rule_any({'+defines' => [$define]});
EOS
  }
  
  for my $ldflag (@$ldflags) {
    $config_global_content .= <<"EOS";
\$config_global->add_ldflag($ldflag);
EOS
  }
  
  $config_global_content .= <<"EOS";
\$config_global;
EOS
  
  # --- Write to file ---
  open my $fh, '>', $config_global_file or die "Can't open file '$config_global_file': $!";
  print $fh $config_global_content;
}

1;

=head1 Name 

SPVM Makefile.PL

=head1 Description

Makefile.PL for L<SPVM> build.

These options are also available in the C<Makefile.PL> generated by L<spvmdist>.

=head1 Usage

  # Basic build
  perl Makefile.PL

  # Debug build with specific compiler flags
  perl Makefile.PL --debug
  
  # Debug build with specific compiler flags and ASan on Linux
  perl Makefile.PL --debug --asan-on-linux
   
  # Parallel build and test
  perl Makefile.PL --parallel-make --parallel-test
  
  # Generate metadata only
  perl Makefile.PL --meta

=head1 Command Line Options

=head2 --ccflag

Specifies additional compiler flags.

=head2 --define

Adds C<-D> macros to the compiler.

=head2 --ldflag

Specifies additional linker flags.

=head2 --debug

Sets the build type L</"--build-type"> to C<Debug>.

=head2 --build-type

Specifies the build type. The possible values are C<Debug>, C<Release>, C<RelWithDebInfo>, or C<MinSizeRel>.

=head2 --asan-on-linux

Enables AddressSanitizer (ASan) for memory error detection on Linux.

=head2 --no-build-spvm-modules

Disables the build of SPVM native and precompile modules.

=head2 --meta

Runs in metadata generation mode.

=head2 --parallel-make

Enables parallel builds using multiple CPU cores.

=head2 --parallel-test

Enables parallel execution of tests.

=head2 --jobs

Specifies the number of parallel jobs for the build and testing. Defaults to C<CPU count + 2> (max 16).

=cut
